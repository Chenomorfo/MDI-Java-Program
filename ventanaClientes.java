    public  void RefrescarLista()
    {
        tblClientes = new DefaultTableModel();
        tablaClientes.clearSelection();
        
        String[] cabecera ={"Numero","Nombre","RFC","Direccion"};
        tblClientes.setColumnIdentifiers(cabecera);
        tablaClientes.setModel(tblClientes);
        
        Object[] fila = new Object[tablaClientes.getColumnCount()];

        for (Cliente unCliente : ventanaPrincipal.listaClientes) {
       
            fila[0] = unCliente.getNumero();
            fila[1] =unCliente.getNombre();
            fila[2] =unCliente.getRFC();
            fila[3]=unCliente.getDir();
            tblClientes.addRow(fila);
        }
        tablaClientes.setModel(tblClientes);
    }
    
    
    
    
